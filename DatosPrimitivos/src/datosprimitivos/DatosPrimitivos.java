package datosprimitivos;

/**
 *
 * @author ander
 */
public class DatosPrimitivos {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        byte b=12;
        short s=4121;
        int i=1542245;
        long l=12498554L;
        
        float f=12.21547F;
        double d=42.1587469854126;
        
        char c='S';
        
        boolean v=false;
        
        System.out.println("Dato tipo byte:"+b);
        System.out.println("Dato tipo short:"+s);
        System.out.println("Dato tipo int"+i);
        System.out.println("Dato tipo long"+l);
        System.out.println("Dato tipo float:"+f);
        System.out.println("Dato tipo double"+d);
        System.out.println("Dato tipo char:"+c);
        System.out.println("Dato tipo boolean"+v);
    }
    
}
